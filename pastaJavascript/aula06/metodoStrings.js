/*
    .charAt(posição) -> busca caracter especifico dentro da string
    .length          -> mostra quantas caractes tem a string
    .toUpperCase()   -> deixa tudo maiuscolo
    .toLowerCase()   -> deixa tudo minuscolo
    .substring(1, 3) -> pega aparti do index indicado
    .split('')       -> dividi o texto 
    .replace("", "") -> substituir uma string por outra
    .trim()          -> tira os espaços
    .indexOf()       -> esse método retorna o índice (posição) da primeira vez que um texto especificado entre os parênteses foi encontrado na string.
    .lastIndexOf()   -> esse método retorna o índice da última ocorrência de um texto especificado entre os parênteses foi encontrado na string.
    .search()        -> esse método busca um valor específico e retorna a posição inicial desse valor na primeira vez que ele é encontrado. Semelhante ao indexOf().
    .startsWith()    -> esse método verifica se a string inicia com um valor específico.
    .endsWith()      -> esse método verifica se a string termina com um valor específico.
*/

